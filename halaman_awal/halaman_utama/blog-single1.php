                        Juan Laurent Kokodiputra dan Sandy Ibrahim Azis masin-masing mengemas 12 angka bagi Satria Muda, serta sama-sama memenangkan delapan rebound.</p>
                    <p>Pelita memanfaatkan 18 kali turn over Satria Muda untuk dikonversi menjadi 23 angka, sementara 12 kali turn over Pelita menghasilkan 13 poin bagi Satria Muda. Satria Muda memimpin pada kuarter pertama. Hardianus Lakudu dkk mengumpulkan
                        17 poin, sementara Prastawa dan rekan-rekan mengoleksi 14 angka pada kuarter awal ini. Pada kuarter kedua, Pelita makin mendekat dan memangkas selisih tinggal satu bola setelah mampu mencetak 16 angka berbanding 14 angka dari Satria
                        Muda. Satria Muda hanya unggul satu angka menutup babak pertama, 31-30. Govinda Saputera membuka kuarter ketiga dengan tiga angka untuk Pelita, emmbuat timnya unggul 33-31. Dua free throw Arki Dikania Wisnu menyamakan kedudukan,
                        namun Prastawa dan Govinda kembali menjauhkan. Juan dan Abbiyu Ramadhan berbalas poin. Lima menit awal kuarter ketiga Pelita memimpin lima angka, 40-35.</p>
                    <p>Laurentius Oei dan Avan Seputra membuat kedudukan kembali imbang, Widyanta Putra Teja bahkan membuat Satria Muda memimpin, namun Yesaya dan Prastawa mengembalikan keunggulan Pelita. Laurentius dan Prastawa berbagi poin, dan Sandy mencetak
                        angka terakhir kuarter ketiga. Pelita memimpin tiga angka, 48-45. Yesaya membuka poin kuarter terakhir dengan driving lay up, Prastawa menambahnya dengan tembakan tiga angka. Satu free throw Aldy Izzatul Rachman dibalas points
                        Hardianus. Tiga angka dari Govinda membuat Pelita memimpin pada lima menit terakhir, 57-48.</p>
                    <p>Dua kali beruntun tembakan tiga angka Sandy memberi harapan pada Satria Muda, memaksa pelatih Pelita asal Serbia, Djordje Jovicic meminta time out. Usai time out Yesaya sukses melesakkan free throw, Sandy membalas dengan dunk. Prastawa
                        dan Laurentius bergantian membuat poin. Prastawa memberi momentum bagus bagi Pelita dengan tembakan tiga angka, 63-58. Satu free throw Widyanta dibalas floating jump Prastawa. Arighi sukses mengeksekusi dua lemparan bebas, Sandy
                        kembali memberi harapan dengan tembakan tiga angka saat waktu tersisa 54 detik. Arighi kembali memasukkan satu free throw Pelita memimpin 68-62, waktu tinggal 38 detik. Satu free throw Vincent menambah poin diwaktu tersisa 27 detik,
                        69-62.</p>
