                    <p>Pelita Jaya Bakrie Basketball tampil sebagai juara IBL Indonesia Cup 2022 setelah dalam final sengit menundukkan musuh bebuyutannya Satria Muda Pertamina 70- 62 di Sritex Arena, Solo, Jawa Tengah, Minggu 13 November.</p>
