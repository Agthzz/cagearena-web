                    <h2 class="mb-3">TIMNAS 3X3 MULAI PEMUSATAN LATIHAN 4 APRIL MENDATANG</h2>
                    <p>Timnas 3x3 putra akan melakukan pemusatan latihan untuk SEA Games mulai 4 April 2022 mendatang. Rencananya ada tiga tempat yang menjadi opsi pemusatan latihan, yaitu GBK Arena, Bogor, dan Krakatau Steel.</p>
                    <p>
                        <img src="images/blog-timnas_3x3.jpg" alt="" class="img-fluid">
                    </p>
                    <p>PP Perbasi akan memanggil 10 pemain untuk mengikuti pemusatan latihan tersebut. Mereka akan memillih kombinasi pemain senior dan junior yang berada di bawah usia 23 tahun untuk regenerasi.</p>
                    <p>TDi dalam daftar tersebut ada pemain senior Surliyadin, Sandy Ibrahim Azis, Oki Wira Sanjaya, dan Jamarr Andre Johnson. Tiga nama pertama merupakan pemain yang memperkuat timnas saat meraih perak di SEA Games 2019 lalu. Kemudia ada
                        Julian Chalias, Serigne Modou Kane, dan Dame Diagne. Sedangkan tiga pemain lainnya akan diumumkan menjelang pemusatan latihan. Menurut pelatih Ali Budimansyah ada beberapa alasan memilih pemain-pemain senior tersebut. Jamarr dipilih
                        karena pertimbangan size dan menggantikan posisi Rivaldo Pangesthio.</p>
                    <p>“Saya tertantang karena kondisi pemain sedang bagus. Mereka masih berkompetisi di IBL 2022. Artinya, secara fisik dan skill individu terasah. Tinggal nanti diperkuat mindset permainan saja karena 5x5 dan 3x3 ada perbedaan,” ujarnya.
                        SEA Games akan berlangsung pada 12-23 Mei 2022 di Vietnam. Sebagai persiapan, timnas berencana menggelar try out ke Jepang selama lima hari. “Jepang saya rekomendasikan menjadi negara tujuan karena mereka memiliki atmosfer 3x3
                        lebih baik. Try out menjadi solusi terbaik di saat kita tidak ada kegiatan sejak Covid-19. Saya ingin simulasi pertandingan di SEA Games saat berada di Jepang. Ini agar para pemain terbiasa,” kata Ali dalam keterangan resminya.</p>
                    <p>Filipina tetap menjadi pesaing terberat bagi timnas Indonesia. Ali menambahkan peluang untuk merebut emas tetap terbuka. Dia melihat tim Filipina pada SEA Games nanti tidak akan sekuat edisi sebelumnya. “Saat itu Filipina menjadi tuan
                        rumah. Mereka menurunkan kekuatan terbaik. Untuk di Vietnam nanti, mereka (Filipina) menurunkan pemain yang berbeda yang kualitasnya berbeda di bawah SEA Games lalu,” lanjut Ali. (rag)
                    </p>

